async function main() {
  console.log('Seeding additional dictionary articles...');
  for (const article of additionalArticles) {
    await prisma.mediaArticle.upsert({
      where: { slug: article.slug },
      update: article,
      create: {
        ...article,
        published_at: new Date(),
      },
    });
    console.log(`- ${article.title}`);
  }
  console.log('Done!');
}

main()
  .catch((e) => {
    console.error(e);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
