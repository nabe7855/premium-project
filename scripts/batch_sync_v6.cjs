async function syncToDb() {
  console.log('Starting DB Sync for Batch 3 (30 hotels)...');
  const jsonContent = JSON.parse(fs.readFileSync(JSON_PATH, 'utf8'));

  for (const [id, content] of Object.entries(batchData)) {
    try {
      console.log(`Processing: ${id}...`);

      // 1. Update lh_hotels
      await prisma.lh_hotels.update({
        where: { id: id },
        data: {
          ai_description: content.ai_description,
          ai_summary: content.ai_summary,
          ai_pros_cons: content.ai_pros_cons,
        },
      });

      // 2. Insert new reviews
      for (const r of content.ai_reviews) {
        await prisma.lh_reviews.create({
          data: {
            id: uuidv4(),
            hotel_id: id,
            user_name: r.userName,
            content: r.content,
            rating: r.rating || 5,
            is_verified: true,
            created_at: new Date(),
          },
        });
      }

      // 3. Update JSON status
      if (jsonContent[id]) {
        Object.assign(jsonContent[id], content);
        jsonContent[id].processing_status = 'completed';
      }

      console.log(`  ✅ Success: ${id}`);
    } catch (err) {
      // Record not found is expected if schema/db mismatch for some minor IDs, skip
      if (err.code === 'P2025') {
        console.warn(`  ⚠️ Skip (Not in DB): ${id}`);
        if (jsonContent[id]) jsonContent[id].processing_status = 'skipped_db_missing';
      } else {
        console.error(`  ❌ Error syncing ${id}:`, err.message);
      }
    }
  }

  fs.writeFileSync(JSON_PATH, JSON.stringify(jsonContent, null, 2));
  console.log('\nBatch 3 Sync finished. JSON updated.');
}

syncToDb()
  .catch((e) => console.error(e))
  .finally(async () => {
    await prisma.$disconnect();
  });
